#String Methods & Functions 
name = "SaHiL DuDwAdKaR"

#Len function
length = len(name)
print(length)

#lower() Method

lower = name.lower()
print(lower)

#upper() Method
upper = name.upper()
print(upper)

#title() Method
title = name.title()
print(title)

#count() Method
count = name.count("D")
print(count)